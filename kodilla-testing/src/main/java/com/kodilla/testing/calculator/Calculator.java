package com.kodilla.testing.calculator;



public class Calculator{

    private int a;
    private int b;
    private int result;

    public Calculator(int a, int b){
        this.a = a;
        this.b = b;
    }
    public int addAToB(){
        return a + b;
    }

    public int substractBFromA(int a, int b){
        return a - b;
    }

    public int getResultAdd (){
        return result = a + b;

    }
    public int getResultSubstract(){
        return result = a - b;
    }
}



 
